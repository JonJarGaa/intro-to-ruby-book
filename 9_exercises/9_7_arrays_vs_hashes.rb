What's the major difference between an Array and a Hash?

MY ANSWER: 

Arrays are ordered collections of elements of various data types 
set to a variable name. Each element is specified with an array location.

Hashes are collections of paired data sets each with a key and value.
Hashes are not necessarily ordered (they are after Ruby 1.9), and 
the values can be called by using it's corresponding key.

Both can receive blocks, but one is a list of elements, the other is a list 
of paired elements. 

BOOK ANSWER: 
The major difference between an array and a hash is that a hash contains a 
key value pair for referencing by key.

