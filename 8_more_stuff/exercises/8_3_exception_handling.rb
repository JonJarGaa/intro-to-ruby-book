What is exception handling and what problem does it solve?


MY ANSWER: 
Exeption handling sets an additional condition using 
reserved "begin, rescue, end" syntax to allow for 
potential for unpredictable exeptions/ errors raised 
from program bugs/infiniteloops/errors, nil values or 
unexpected/incompatible input. It allows for the exception 
without halting the program so it can continue.

BOOK SOLUTION:

Exception handling is a structure used to handle the possibility 
of an error occurring in a program. It is a way of handling the 
error by changing the flow of control without exiting the program 
entirely.